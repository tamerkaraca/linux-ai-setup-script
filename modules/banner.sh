print_banner_block() {
    local -n ref="$1"
    local idx=0
    local total=${#RAINBOW_COLORS[@]}
    for line in "${ref[@]}"; do
        local color="${RAINBOW_COLORS[$((idx % total))]}"
        echo -e "${color}${line}${NC}"
        idx=$((idx + 1))
    done
}

print_info_panel() {
    local version="$1"
    local repo="$2"
    echo -e "${BLUE}╔══════════════════════════════════════════════════════════════════════╗${NC}"
    printf "%s\n" "${BLUE}║${NC} ${BOLD}Script Bilgileri${NC}                                                       ${BLUE}║${NC}"
    echo -e "${BLUE}╠══════════════════════════════════════════════════════════════════════╣${NC}"
    printf "%s\n" "${BLUE}║${NC} Versiyon : ${GREEN}${version}${NC}                                                 ${BLUE}║${NC}"
    printf "%s\n" "${BLUE}║${NC} GitHub   : ${CYAN}${repo}${NC} ${BLUE}║${NC}"
    echo -e "${BLUE}╚══════════════════════════════════════════════════════════════════════╝${NC}"
}

render_setup_banner() {
    local version="$1"
    local repo="$2"
    print_banner_block BANNER_AI
    print_banner_block BANNER_AUTHOR
    print_info_panel "$version" "$repo"
    echo
}
